export function countryPrepositional(name: string): string {
  const trimmed = name.trim()
  if (DICT[trimmed]) return DICT[trimmed]
  // эвристика по окончанию
  if (/ия$/.test(trimmed)) return trimmed.replace(/ия$/, 'ии')
  if (/[ая]$/.test(trimmed)) return trimmed.replace(/[ая]$/, 'е')
  if (/ь$/.test(trimmed)) return trimmed.replace(/ь$/, 'и')
  if (/й$/.test(trimmed)) return trimmed.replace(/й$/, 'е')
  return trimmed + 'е'
}

// Извлекает название страны из URL вида /отели/<country>/...
export function extractCountryFromUrl(url: string): string | null {
  try {
    const u = new URL(url)
    const parts = u.pathname.split('/').filter(Boolean).map(p => decodeURIComponent(p))
    // ['отели', 'Страна', ...] или ['hotels', 'Country', ...]
    if (parts.length >= 2) return parts[1].replace(/-/g, ' ')
    return null
  } catch {
    return null
  }
}
